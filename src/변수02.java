public class 변수02 {
    public static void main(String[] args) {
        //정수를 저장할 수 있는 변수 num1 과 num2를 선언하시오.
        int num1,num2;

        //num1 에는 10을, num2에는 20을 저장하시오.
        num1=10; num2=20;
        System.out.println("num1="+num1);
        //num1=10
        System.out.println("num2="+num2);
        //num2=20
        System.out.println("num1+num2="+(num1+num2));
        //num1+num2=30
    }
}
