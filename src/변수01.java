//변수 : 변하는 값을 저장하는 공간.
//변수를 사용하기 위해서는 반드시 변수를 선언해야함.

public class 변수01 {
    public static void main(String[] args) {
        int a;
        //변수 선언 문법 : 자료형 변수명;
        //정수를 담을 수 있는 a라는 이름의 상자(변수)를 만듦.
        //int(정수:integer)
        a=10;
        //=(대입연산자)
        //=를 기준으로 우측항을 좌측항에 대입한다(저장한다)
        System.out.println(a);
        //결과값10
        System.out.println("a");
        //결과값a
    }
}
